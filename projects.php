					<h4>Forensick</h4>
				<h5>Jan 2013 - May 2013</h5>
				</div>	
				<p></p>
				<p>This project involved gamification of <a href="http://web.njit.edu/~geller/" target="_blank">Dr. James Geller's</a> work on auditing the SNOMED CT medical terminology.
				</p>
				<p>Taking over from a student who had recently graduated, I was responsible for continuing the framework
				of a crowdsourced game, title Forensick. The final product is intended to discover terminology
				errors through crowdsourcing, similarly to the University of Washington's Foldit project. 
				</p>
				<p>
				In Forensick, players control a helicopter in a cityscape. Buildings represent categories of terms, which contain boxes that represent SNOMED CT concepts.
				Once inside, players can shoot boxes to signal that they believe a concept has been miscategorized, or pick it up with a claw arm and fly it to
				the correct building.
				</p>
				<p>I worked with a game engine called DarkBasic, which uses a rather outdated procedural language that
				pushed me to be more creative with my coding, as I lacked objects to manipulate.</p>
				<p>
				The gamification aspect of this project appealed to my interactive design sensibilities, and reinforced my desire to pursue interactive media as a possible
				research focus.
				</p>
				<p>I presented my work during an <a href="javascript:void(0)" class="click_popup" title="showcase">undergraduate research showcase</a> held at NJIT.</p>
